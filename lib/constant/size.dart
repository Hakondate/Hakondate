  static const double minimum = 8.0;            // 最小のマージン
  static const double normal = 16.0;            // 通常のマージン
  static const double normalVertical = 30.0;    // 水平方向の基本マージン
  static const double normalHorizontal = 25.0;  // 垂直方向の基本マージン
